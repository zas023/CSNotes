/*包：
1. 为何使用Package
   1.1 解决类之间的重名问题
   1.2 为了方便管理：合适的类位于合适的包名

2. 如何使用
   2.1 通常是类的第一句非注释语句
   2.2 域名倒着写，再加上模块名，并于内部管理类

3. 注：com.gao和com.gao.car,这两个包没有包含关系，是完全独立的。
      只是逻辑上看似后者是前者的一部分
*/


/*JDK中常用包：

1. java.lang（language)
   包含一些Java语言的核心类，如String，Math，Integer，System和Thread，提供常用功能

2. java.awt
   包含抽象窗口工具集（abstract window toolkits）的多个类，这些类用来创建和管理程序的GUI

3. java.net
   包含执行网络相关的操作类

4. java.io
   包含能提供多种IO操作的类

5. java.util
   包含一些实用的工具类，如定义系统特性，使用日期等
*/
